def get_quiz_question(difficulty: str) -> Optional[dict]:
    """Вернуть случайный вопрос заданной сложности."""
    questions = QUESTIONS.get(difficulty, [])
    if not questions:
        return None
    return random.choice(questions)


def check_answer(question: dict, answer_idx: int, user_data: dict) -> tuple[bool, str]:
    """Проверить ответ и обновить статистику."""
    is_correct = answer_idx == question["correct"]
    user_data.setdefault("quiz_correct", 0)
    user_data.setdefault("quiz_wrong", 0)
    if is_correct:
        user_data["quiz_correct"] += 1
    else:
        user_data["quiz_wrong"] += 1
    return is_correct, question["explanation"]


def get_quiz_stats(user_data: dict) -> dict:
    """Вернуть статистику квиза пользователя."""
    correct = user_data.get("quiz_correct", 0)
    wrong = user_data.get("quiz_wrong", 0)
    total = correct + wrong
    accuracy = round(correct / total * 100, 1) if total > 0 else 0
    return {"correct": correct, "wrong": wrong, "total": total, "accuracy": accuracy}
