def _truncate(text: str, max_len: int = 48) -> str:
    """Truncate button text to fit Telegram limits."""
    return text[:max_len] + "вЂ¦" if len(text) > max_len else text


class QuizSession:
    """Manages a single user's quiz session with difficulty levels."""

    LEVELS = {
        "easy": "рџџў Р›С‘РіРєРёР№",
        "medium": "рџџЎ РЎСЂРµРґРЅРёР№",
        "hard": "рџ”ґ РЎР»РѕР¶РЅС‹Р№",
        "mixed": "рџЋІ РњРёРєСЃ",
    }

    def __init__(self, level: str = "mixed", count: int = 5):
        self.level = level
        pool = [q for q in QUIZ_BANK if level == "mixed" or q[4] == level]
        self.questions = random.sample(pool, min(count, len(pool)))
        self.current = 0
        self.score = 0

    def current_question(self) -> str:
        q, options, _, _, diff = self.questions[self.current]
        diff_emoji = {"easy": "рџџў", "medium": "рџџЎ", "hard": "рџ”ґ"}.get(diff, "")
        return (
            f"вќ“ <b>Р’РѕРїСЂРѕСЃ {self.current + 1}/{len(self.questions)}</b> {diff_emoji}\n\n"
            f"{q}"
        )

    def get_answer_keyboard(self) -> InlineKeyboardMarkup:
        """Return inline keyboard with A/B/C/D answer buttons."""
        _, options, _, _, _ = self.questions[self.current]
        buttons = []
        for opt in options:
            letter = opt[0]  # "A", "B", "C", or "D"
            # Show full option text in button
            btn_text = _truncate(opt, 50)
            buttons.append([InlineKeyboardButton(btn_text, callback_data=f"quiz_answer_{letter}")])
        return InlineKeyboardMarkup(buttons)

    def answer_by_letter(self, letter: str) -> str:
        """Process answer by letter. Returns feedback text."""
        q, options, correct, explanation, diff = self.questions[self.current]
        letter = letter.strip().upper()

        if letter not in ("A", "B", "C", "D"):
            return "РџРѕР¶Р°Р»СѓР№СЃС‚Р°, РІС‹Р±РµСЂРёС‚Рµ РѕРґРёРЅ РёР· РІР°СЂРёР°РЅС‚РѕРІ РѕС‚РІРµС‚Р°."

        is_correct = letter == correct
        if is_correct:
            self.score += 1

        self.current += 1
        feedback = "вњ… <b>РџСЂР°РІРёР»СЊРЅРѕ!</b>" if is_correct else f"вќЊ <b>РќРµРІРµСЂРЅРѕ.</b> РџСЂР°РІРёР»СЊРЅС‹Р№ РѕС‚РІРµС‚: <b>{correct}</b>"
        result = f"{feedback}\n\nрџ“– {explanation}"

        if not self.is_finished:
            result += "\n\n" + self.current_question()
        else:
            result += "\n\n" + self.summary()

        return result

    def answer(self, user_answer: str) -> str:
        """Backward compat: accept text input."""
        return self.answer_by_letter(user_answer)

