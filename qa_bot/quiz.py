class QuizSession:
    """Manages a single user's quiz session with difficulty levels."""

    LEVELS = {
        "easy": "🟢 Лёгкий",
        "medium": "🟡 Средний",
        "hard": "🔴 Сложный",
        "mixed": "🎲 Микс",
    }

    def __init__(self, level: str = "mixed", count: int = 5):
        self.level = level
        pool = [q for q in QUIZ_BANK if level == "mixed" or q[4] == level]
        self.questions = random.sample(pool, min(count, len(pool)))
        self.current = 0
        self.score = 0

    def current_question(self) -> str:
        q, options, _, _, diff = self.questions[self.current]
        diff_emoji = {"easy": "🟢", "medium": "🟡", "hard": "🔴"}.get(diff, "")
        opts = "\n".join(options)
        return (
            f"❓ <b>Вопрос {self.current + 1}/{len(self.questions)}</b> {diff_emoji}\n\n"
            f"{q}\n\n{opts}\n\n<i>Ответь: A, B, C или D</i>"
        )

    def answer(self, user_answer: str) -> str:
        q, options, correct, explanation, diff = self.questions[self.current]
        user_answer = user_answer.strip().upper()

        if user_answer not in ("A", "B", "C", "D"):
            return "Пожалуйста, ответь: <b>A</b>, <b>B</b>, <b>C</b> или <b>D</b>"

        is_correct = user_answer == correct
        if is_correct:
            self.score += 1

        self.current += 1
        feedback = "✅ <b>Правильно!</b>" if is_correct else f"❌ <b>Неверно.</b> Правильный ответ: <b>{correct}</b>"
        result = f"{feedback}\n\n📖 {explanation}"

        if not self.is_finished:
            result += "\n\n" + self.current_question()
        else:
            result += "\n\n" + self.summary()

        return result

    def summary(self) -> str:
        pct = int(self.score / len(self.questions) * 100)
        if pct >= 80:
            emoji, msg = "🏆", "Отличный результат!"
        elif pct >= 50:
            emoji, msg = "📚", "Хороший старт, есть куда расти."
        else:
            emoji, msg = "💪", "Продолжай изучать теорию ISTQB!"
        return (
            f"{emoji} <b>Квиз завершён! {msg}</b>\n"
            f"Результат: {self.score}/{len(self.questions)} ({pct}%)\n\n"
            f"🔁 Новый квиз — /quiz\n"
            f"📖 Другие команды — /help"
        )

    @property
    def is_finished(self) -> bool:
        return self.current >= len(self.questions)
